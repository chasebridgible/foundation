#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import {
  appendRunLogEvent,
  buildLegacySliceQueuePayload,
  buildSplitQueuePayload,
  buildSplitQueueReportState,
  defaultBackfillDir,
  ensureDir,
  parseCliArgs,
  readCapabilityMatrixRows,
  readJsonl,
  splitQueuePathFor
} from "./split-queue-core.mjs";

const scriptPath = fileURLToPath(import.meta.url);

function usage() {
  return `Usage:
  npm run foundation:split-queue:report -- --repo /path/to/repo --run-id YYYYMMDD-NN [--report docs/specs/backfill/review-report-YYYYMMDD-NN.html] [--run-log path]

Creates or updates a target backfill report with Split And Queue handoff state, canonical split queue JSON, and legacy backfill-slice-queue compatibility JSON.`;
}

function renderSection(state) {
  return `<section id="split-queue-v1-state" data-spec-section="split-queue-v1-state" data-section-type="handoff">
  <h2>Split And Queue V1 State</h2>
  <table class="status-table">
    <tbody>
      <tr><td>Queue</td><td><code>${state.queuePath}</code></td></tr>
      <tr><td>Capability rows</td><td>${state.capabilityCount}</td></tr>
      <tr><td>Needs-split capabilities</td><td>${state.needsSplitCount}</td></tr>
      <tr><td>Unresolved needs-split capabilities</td><td>${state.unresolvedNeedsSplitCount}</td></tr>
      <tr><td>Queue slices</td><td>${state.queueSliceCount}</td></tr>
      <tr><td>Pending slices</td><td>${state.pendingCount}</td></tr>
      <tr><td>Ready slices</td><td>${state.readyCount}</td></tr>
      <tr><td>In-progress slices</td><td>${state.inProgressCount}</td></tr>
      <tr><td>Acceptable slices</td><td>${state.acceptableCount}</td></tr>
      <tr><td>Blocked slices</td><td>${state.blockedCount}</td></tr>
      <tr><td>Out-of-scope slices</td><td>${state.outOfScopeCount}</td></tr>
      <tr><td>Current slice</td><td>${state.currentSliceId ? `<code>${state.currentSliceId}</code>` : "none"}</td></tr>
      <tr><td>Checker result</td><td>${state.checkerResult}</td></tr>
      <tr><td>Eval result</td><td>${state.evalResult}${state.evalScore === null ? "" : `, score ${state.evalScore}`}</td></tr>
      <tr><td>Eval revision targets</td><td>${state.evalRevisionTargetCount}</td></tr>
      <tr><td>Eval warnings</td><td>${state.evalWarningCount}</td></tr>
      <tr><td>Eval blocking findings</td><td>${state.evalBlockingFindingCount}</td></tr>
      <tr><td>Eval receipt</td><td><code>${state.evalReceiptPath}</code></td></tr>
      <tr><td>Summary</td><td><code>${state.summaryPath}</code></td></tr>
      <tr><td>Latest run-log sequence</td><td>${state.latestRunLogSequence ?? "not recorded"}</td></tr>
      <tr><td>Next layer</td><td>${state.nextLayer}</td></tr>
    </tbody>
  </table>
  <p>Evidence Pack may only consume this layer when the next layer is <code>evidence pack</code>; otherwise rows named by check/eval receipts must return to the Split And Queue fill loop.</p>
</section>`;
}

function renderQueueRows(payload) {
  const rows = payload.queue.length === 0
    ? "<tr><td colspan=\"6\">No queue slices.</td></tr>"
    : payload.queue.map(row => `<tr><td><code>${row.sliceId}</code></td><td>${row.name}</td><td>${row.status}</td><td>${row.ownerSkill}</td><td>${row.upstreamCapabilityIds.length}</td><td>${row.nextAction || ""}</td></tr>`).join("\n");
  return `<section id="split-queue-v1-table" data-spec-section="split-queue-v1-table" data-section-type="state">
  <h2>Split And Queue Rows</h2>
  <table class="status-table">
    <thead><tr><th>ID</th><th>Name</th><th>Status</th><th>Owner</th><th>Capabilities</th><th>Next action</th></tr></thead>
    <tbody>${rows}</tbody>
  </table>
</section>`;
}

function renderReport(state, splitQueuePayload, legacyPayload) {
  return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>Split And Queue Report ${state.runId}</title>
<link rel="stylesheet" href="../spec-system.css">
<script type="application/json" id="backfill-split-queue-state">
${JSON.stringify(state, null, 2)}
</script>
<script type="application/json" id="backfill-split-queue">
${JSON.stringify(splitQueuePayload, null, 2)}
</script>
<script type="application/json" id="backfill-slice-queue">
${JSON.stringify(legacyPayload, null, 2)}
</script>
</head>
<body>
<main class="main">
  <section id="summary">
    <div class="spec-eyebrow">docs/specs/backfill/review-report-${state.runId}.html</div>
    <h1>Backfill Report ${state.runId}</h1>
    <p class="lede">Minimal report generated by the Foundation Split And Queue V1 report integration.</p>
  </section>
  <article class="doc-body">
${renderSection(state)}
${renderQueueRows(splitQueuePayload)}
  </article>
</main>
</body>
</html>
`;
}

function upsertReportState(html, state, splitQueuePayload, legacyPayload) {
  const stateScript = `<script type="application/json" id="backfill-split-queue-state">\n${JSON.stringify(state, null, 2)}\n</script>`;
  const splitQueueScript = `<script type="application/json" id="backfill-split-queue">\n${JSON.stringify(splitQueuePayload, null, 2)}\n</script>`;
  const legacyScript = `<script type="application/json" id="backfill-slice-queue">\n${JSON.stringify(legacyPayload, null, 2)}\n</script>`;
  const section = `${renderSection(state)}\n${renderQueueRows(splitQueuePayload)}`;
  let nextHtml = html;
  for (const [id, script] of [
    ["backfill-split-queue-state", stateScript],
    ["backfill-split-queue", splitQueueScript],
    ["backfill-slice-queue", legacyScript]
  ]) {
    const pattern = new RegExp(`<script\\b[^>]*id=["']${id}["'][^>]*>[\\s\\S]*?<\\/script>`, "i");
    if (pattern.test(nextHtml)) {
      nextHtml = nextHtml.replace(pattern, script);
    } else {
      nextHtml = nextHtml.replace("</head>", `${script}\n</head>`);
    }
  }

  if (/<section\b[^>]*id=["']split-queue-v1-state["'][^>]*>[\s\S]*?<\/section>\s*<section\b[^>]*id=["']split-queue-v1-table["'][^>]*>[\s\S]*?<\/section>/i.test(nextHtml)) {
    nextHtml = nextHtml.replace(/<section\b[^>]*id=["']split-queue-v1-state["'][^>]*>[\s\S]*?<\/section>\s*<section\b[^>]*id=["']split-queue-v1-table["'][^>]*>[\s\S]*?<\/section>/i, section);
  } else if (/<section\b[^>]*id=["']split-queue-v1-state["'][^>]*>[\s\S]*?<\/section>/i.test(nextHtml)) {
    nextHtml = nextHtml.replace(/<section\b[^>]*id=["']split-queue-v1-state["'][^>]*>[\s\S]*?<\/section>/i, section);
  } else if (/<article\b[^>]*class=["']doc-body["'][^>]*>/i.test(nextHtml)) {
    nextHtml = nextHtml.replace(/<article\b([^>]*)class=["']doc-body["']([^>]*)>/i, match => `${match}\n${section}`);
  } else {
    nextHtml = nextHtml.replace("</main>", `${section}\n</main>`);
  }
  return nextHtml;
}

function main() {
  const options = parseCliArgs(process.argv.slice(2));
  if (options.help) {
    console.log(usage());
    return;
  }
  if (!options.repo) throw new Error("Missing --repo");
  if (!options["run-id"]) throw new Error("Missing --run-id");

  const repoRoot = path.resolve(options.repo);
  const runId = options["run-id"];
  const outDir = options["out-dir"] ? path.resolve(repoRoot, options["out-dir"]) : defaultBackfillDir(repoRoot);
  const reportPath = options.report
    ? path.resolve(repoRoot, options.report)
    : path.join(outDir, `review-report-${runId}.html`);
  const runLogPath = options["run-log"] ? path.resolve(repoRoot, options["run-log"]) : null;
  const capabilityRows = readCapabilityMatrixRows(repoRoot, runId, outDir).rows;
  const queueRows = readJsonl(splitQueuePathFor(repoRoot, runId, outDir)).rows;
  let state = buildSplitQueueReportState({ repoRoot, runId, outDir, capabilityRows, queueRows, runLogPath });
  let splitQueuePayload = buildSplitQueuePayload({ runId, repoRoot, queueRows });
  let legacyPayload = buildLegacySliceQueuePayload({ runId, repoRoot, queueRows });

  appendRunLogEvent(runLogPath, {
    runId,
    slice: state.currentSliceId,
    phase: "handoff",
    event: "handoff",
    summary: `Split And Queue handoff recorded with next layer ${state.nextLayer}.`,
    artifactsRead: [
      state.queuePath,
      state.checkerPath,
      state.evalReceiptPath
    ],
    artifactsChanged: [path.relative(repoRoot, reportPath)],
    commands: ["foundation:split-queue:report"],
    checks: [{ name: "split-queue-handoff", result: state.nextLayer === "evidence pack" ? "passed" : "needs-revision" }],
    result: state.nextLayer === "evidence pack"
      ? "Split And Queue handoff gate passed."
      : "Split And Queue handoff gate did not pass.",
    nextAction: state.nextLayer === "evidence pack"
      ? "Evidence Pack may consume Split And Queue artifacts."
      : "Revise Split And Queue rows named by checker or eval receipts."
  });
  state = buildSplitQueueReportState({ repoRoot, runId, outDir, capabilityRows, queueRows, runLogPath });
  splitQueuePayload = buildSplitQueuePayload({ runId, repoRoot, queueRows });
  legacyPayload = buildLegacySliceQueuePayload({ runId, repoRoot, queueRows });

  ensureDir(path.dirname(reportPath));
  const nextHtml = fs.existsSync(reportPath)
    ? upsertReportState(fs.readFileSync(reportPath, "utf8"), state, splitQueuePayload, legacyPayload)
    : renderReport(state, splitQueuePayload, legacyPayload);
  fs.writeFileSync(reportPath, nextHtml, "utf8");

  console.log(JSON.stringify({
    reportPath: path.relative(repoRoot, reportPath),
    state
  }, null, 2));
}

if (process.argv[1] && path.resolve(process.argv[1]) === scriptPath) {
  try {
    main();
  } catch (error) {
    console.error(error.message);
    console.error("");
    console.error(usage());
    process.exit(2);
  }
}
