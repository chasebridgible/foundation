#!/usr/bin/env node
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import {
  createManifest,
  defaultBackfillDir,
  ensureDir,
  evalReceiptPathFor,
  evalSummaryPathFor,
  manifestPathFor,
  parseCliArgs,
  readJson,
  readJsonl,
  registryPathFor,
  checkPathFor,
  writeJson,
  writeJsonl
} from "./file-registry-core.mjs";

const scriptPath = fileURLToPath(import.meta.url);

function usage() {
  return `Usage:
  npm run foundation:file-registry:report -- --repo /path/to/repo --run-id YYYYMMDD-NN [--report docs/specs/backfill/review-report-YYYYMMDD-NN.html] [--run-log path]

Creates or updates a target backfill report with the File Registry V1 handoff state.`;
}

function latestRunLogSequence(logPath) {
  if (!logPath || !fs.existsSync(logPath)) return null;
  const parsed = readJsonl(logPath);
  const sequences = parsed.rows.map(row => row.sequence).filter(Number.isInteger);
  return sequences.length > 0 ? Math.max(...sequences) : null;
}

function readEvalSummary(receiptPath) {
  if (!fs.existsSync(receiptPath)) return null;
  const parsed = readJsonl(receiptPath);
  return parsed.rows.find(row => row.receiptType === "summary") || null;
}

function buildState({ repoRoot, runId, outDir, runLogPath }) {
  const registryPath = registryPathFor(repoRoot, runId, outDir);
  const registry = readJsonl(registryPath);
  const checkPath = checkPathFor(repoRoot, runId, outDir);
  const check = fs.existsSync(checkPath) ? readJson(checkPath) : null;
  const evalReceiptPath = evalReceiptPathFor(repoRoot, runId, outDir);
  const evalSummary = readEvalSummary(evalReceiptPath);
  const pendingCount = registry.rows.filter(row => row.status === "pending").length;
  const blockingFlagCount = registry.rows.filter(row => (row.reviewFlags || []).some(flag => flag.severity === "blocking")).length;

  return {
    schema: "foundation.backfill.file-registry-report-state.v1",
    runId,
    generatedAt: new Date().toISOString(),
    registryPath: path.relative(repoRoot, registryPath),
    manifestPath: `docs/specs/backfill/file-manifest-${runId}.json`,
    checkerPath: path.relative(repoRoot, checkPath),
    checkerResult: check?.summary?.fail === 0 ? "pass" : "fail-or-missing",
    evalReceiptPath: path.relative(repoRoot, evalReceiptPath),
    evalSummaryPath: path.relative(repoRoot, evalSummaryPathFor(repoRoot, runId, outDir)),
    evalResult: evalSummary?.acceptabilityGate?.acceptable ? "pass" : "fail-or-missing",
    evalScore: evalSummary?.totalScore ?? null,
    pendingCount,
    mappedCount: registry.rows.filter(row => row.status === "mapped").length,
    blockingFlagCount,
    latestRunLogSequence: latestRunLogSequence(runLogPath),
    nextLayer: pendingCount === 0 && blockingFlagCount === 0 && check?.summary?.fail === 0 && evalSummary?.acceptabilityGate?.acceptable
      ? "surface registry"
      : "file registry revision"
  };
}

function renderSection(state) {
  return `<section id="file-registry-v1-state" data-spec-section="file-registry-v1-state" data-section-type="handoff">
  <h2>File Registry V1 State</h2>
  <table class="status-table">
    <tbody>
      <tr><td>Registry</td><td><code>${state.registryPath}</code></td></tr>
      <tr><td>Pending rows</td><td>${state.pendingCount}</td></tr>
      <tr><td>Mapped rows</td><td>${state.mappedCount}</td></tr>
      <tr><td>Blocking flags</td><td>${state.blockingFlagCount}</td></tr>
      <tr><td>Checker result</td><td>${state.checkerResult}</td></tr>
      <tr><td>Eval result</td><td>${state.evalResult}${state.evalScore === null ? "" : `, score ${state.evalScore}`}</td></tr>
      <tr><td>Eval receipt</td><td><code>${state.evalReceiptPath}</code></td></tr>
      <tr><td>Eval summary</td><td><code>${state.evalSummaryPath}</code></td></tr>
      <tr><td>Latest run-log sequence</td><td>${state.latestRunLogSequence ?? "not recorded"}</td></tr>
      <tr><td>Next layer</td><td>${state.nextLayer}</td></tr>
    </tbody>
  </table>
</section>`;
}

function renderReport(state) {
  return `<!DOCTYPE html>
<html lang="en">
<head>
<meta charset="UTF-8">
<meta name="viewport" content="width=device-width, initial-scale=1.0">
<title>File Registry Report ${state.runId}</title>
<link rel="stylesheet" href="../spec-system.css">
<script type="application/json" id="backfill-file-registry-state">
${JSON.stringify(state, null, 2)}
</script>
</head>
<body>
<main class="main">
  <section id="summary">
    <div class="spec-eyebrow">docs/specs/backfill/review-report-${state.runId}.html</div>
    <h1>Backfill Report ${state.runId}</h1>
    <p class="lede">Minimal report generated by the Foundation File Registry V1 report integration.</p>
  </section>
  <article class="doc-body">
${renderSection(state)}
  </article>
</main>
</body>
</html>
`;
}

function upsertReportState(html, state) {
  const script = `<script type="application/json" id="backfill-file-registry-state">\n${JSON.stringify(state, null, 2)}\n</script>`;
  const section = renderSection(state);
  let nextHtml = html;
  if (/<script\b[^>]*id=["']backfill-file-registry-state["'][^>]*>[\s\S]*?<\/script>/i.test(nextHtml)) {
    nextHtml = nextHtml.replace(/<script\b[^>]*id=["']backfill-file-registry-state["'][^>]*>[\s\S]*?<\/script>/i, script);
  } else {
    nextHtml = nextHtml.replace("</head>", `${script}\n</head>`);
  }

  if (/<section\b[^>]*id=["']file-registry-v1-state["'][^>]*>[\s\S]*?<\/section>/i.test(nextHtml)) {
    nextHtml = nextHtml.replace(/<section\b[^>]*id=["']file-registry-v1-state["'][^>]*>[\s\S]*?<\/section>/i, section);
  } else if (/<article\b[^>]*class=["']doc-body["'][^>]*>/i.test(nextHtml)) {
    nextHtml = nextHtml.replace(/<article\b([^>]*)class=["']doc-body["']([^>]*)>/i, match => `${match}\n${section}`);
  } else {
    nextHtml = nextHtml.replace("</main>", `${section}\n</main>`);
  }
  return nextHtml;
}

function refreshRegistryRowsForPaths({ repoRoot, runId, outDir, relativePaths }) {
  const manifestPath = manifestPathFor(repoRoot, runId, outDir);
  const registryPath = registryPathFor(repoRoot, runId, outDir);
  if (!fs.existsSync(manifestPath) || !fs.existsSync(registryPath)) return;

  const currentManifest = createManifest({ repoRoot, runId });
  const currentByPath = new Map(currentManifest.files.map(file => [file.path, file]));
  const paths = new Set(relativePaths);
  const manifest = readJson(manifestPath);
  let changed = false;

  manifest.files = manifest.files.map(file => {
    if (!paths.has(file.path) || !currentByPath.has(file.path)) return file;
    changed = true;
    return currentByPath.get(file.path);
  });
  if (changed) {
    manifest.counts = {
      ...manifest.counts,
      totalFiles: manifest.files.length
    };
    writeJson(manifestPath, manifest);
  }

  const registry = readJsonl(registryPath);
  const rows = registry.rows.map(row => {
    const current = currentByPath.get(row.path);
    return paths.has(row.path) && current ? { ...row, ...current } : row;
  });
  writeJsonl(registryPath, rows);
}

function main() {
  const options = parseCliArgs(process.argv.slice(2));
  if (options.help) {
    console.log(usage());
    return;
  }
  if (!options.repo) throw new Error("Missing --repo");
  if (!options["run-id"]) throw new Error("Missing --run-id");

  const repoRoot = path.resolve(options.repo);
  const runId = options["run-id"];
  const outDir = options["out-dir"] ? path.resolve(repoRoot, options["out-dir"]) : defaultBackfillDir(repoRoot);
  const reportPath = options.report
    ? path.resolve(repoRoot, options.report)
    : path.join(outDir, `review-report-${runId}.html`);
  const runLogPath = options["run-log"] ? path.resolve(repoRoot, options["run-log"]) : null;
  const state = buildState({ repoRoot, runId, outDir, runLogPath });

  ensureDir(path.dirname(reportPath));
  const nextHtml = fs.existsSync(reportPath)
    ? upsertReportState(fs.readFileSync(reportPath, "utf8"), state)
    : renderReport(state);
  fs.writeFileSync(reportPath, nextHtml, "utf8");
  refreshRegistryRowsForPaths({
    repoRoot,
    runId,
    outDir,
    relativePaths: [path.relative(repoRoot, reportPath).split(path.sep).join("/")]
  });

  console.log(JSON.stringify({
    reportPath: path.relative(repoRoot, reportPath),
    state
  }, null, 2));
}

if (process.argv[1] && path.resolve(process.argv[1]) === scriptPath) {
  try {
    main();
  } catch (error) {
    console.error(error.message);
    console.error("");
    console.error(usage());
    process.exit(2);
  }
}
